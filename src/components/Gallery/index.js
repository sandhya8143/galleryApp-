class Gallery extends Component {
  state = {
    listItem: imagesList[0].imageUrl,
    listImage: imagesList[0].imageAltText,
  }

  clickedImage = (imageUrl, imageAltText) => {
    this.setState({listItem: imageUrl, listImage: imageAltText})
  }

  render() {
    const {listItem, listImage} = this.state
    return (
      <div className="divContainer">
        <div className="div">
          <img className="imageEl" src={listItem} alt={listImage} />
          <h1>Nature Photography</h1>
          <p>Nature Photography by Rahul</p>
          <ul className="listStyle">
            {imagesList.map(eachItem => (
              <ThumbnailItem
                thumbList={eachItem}
                clickedImage={this.clickedImage}
                key={eachItem.id}
                activeImage={listItem === eachItem.imageUrl}
              />
            ))}
          </ul>
        </div>
      </div>
    )
  }
}
